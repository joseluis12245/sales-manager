formatDate(date = new Date()) {
    let d = new Date(date),
    month = '' + (d.getMonth() + 1),
    day = '' + d.getDate(),
    year = d.getFullYear();

    if (month.length < 2) 
        month = '0' + month;
    if (day.length < 2) 
        day = '0' + day;

    return [year, month, day].join('-');
},

calculateRemaininDays(date){
  let oldDate = new Date(date)
  let remainingDays = date - oldDate;

  return remainingDays;
}
